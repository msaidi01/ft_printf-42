#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;
	int	r;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	i = ft_strlen(s);
	r = write(1, s, i);
	if (r < 0)
		return (-1);
	return (i);
}
